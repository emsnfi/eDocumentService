				<h4 itemprop="serviceType"><b>一、國際上對身心障礙族群的著作權合理使用規定情形及趨勢</b></h4>
				<blockquote>
					<p>2003年世界盲人組織調查發現，全球有三億多視障者，97%處於開發中國家，卻只能接受5%的資訊。身心障礙族群屬於社會之弱勢團體，其於資訊尋求過程中對於訊息的掌控能力較為薄弱，如能依法利用他人著作，除能促使身心障礙者之心智提昇，協助身心障礙者投入社會外，更能達成國家文化全面性的發展。況身心障礙族群屬社會之少數，於利用著作上，對於著作財產權人之影響亦屬輕微。</p>
