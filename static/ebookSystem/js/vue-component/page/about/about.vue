				<blockquote>
					<p>為符馬拉喀什條約之精神，我國於2014年1月22日修正並經立法院三讀通過著作權法第53條、第80條之2及第87條之1等規定，將重製問題從法律面至實務面全面改良制度，希望使各種出版品能更快速、更普及的為特定身心障礙者提供重製物，增加其接觸資訊的機會，而著作權法(53、80-2、87-1)修正條文修改詳細內容如下：</p>
					<p>著作權法第53條－擴大重製權利、重製物可在免責人間互相流通傳輸</p>
					<small>
						著作權法第53條原文：<br />「中央或地方政府機關、非營利機構或團體、依法立案之各級學校，為專供視覺障礙者、學習障礙者、聽覺障礙者或其他感知著作有困難之障礙者使用之目的，得以翻譯、點字、錄音、數位轉換、口述影像、附加手語或其他方式利用已公開發表之著作。前項所定障礙者或其代理人為供該障礙者個人非營利使用，準用前項規定。依前二項規定製作之著作重製物，得於前二項所定障礙者、中央或地方政府機關、非營利機構或團體、依法立案之各級學校間散布或公開傳輸。」<br />
